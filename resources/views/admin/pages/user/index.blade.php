            }
        });
    </script>
    <script>
        $(function() {
            let otable = $('#dTable').DataTable({
                processing: true,
                serverSide: true,
                ajax: '{{ route('admin.users.data') }}',
                columns: [{
                        data: 'DT_RowIndex',
                        name: 'DT_RowIndex',
                        orderable: false,
                        searchable: false
                    },
                    {
                        data: 'avatar',
                        name: 'avatar'
                    },
                    {
                        data: 'name',
                        name: 'name'
                    },
                    {
                        data: 'username',
                        name: 'username'
                    },
                    {
                        data: 'email',
                        name: 'email'
                    },
                    {
                        data: 'role',
                        name: 'role'
                    },
                    {
                        data: 'status',
                        name: 'status'
                    },
                    {
                        data: 'action',
                        name: 'action',
                        orderable: false,
                        searchable: false
                    }
                ]
            });
            $('.btnAdd').on('click', function() {
                $.ajax({
                    url: '{{ route('admin.roles.get') }}',
                    type: 'POST',
                    dataType: 'JSON',
                    success: function(response) {
                        $('#myModal #role').empty();
                        response.forEach(res => {
                            $('#myModal #role').append(
                                `<option class="${res.name}">${res.name}</option>`);
                        });
                    }
                })
                $('#myForm .d-pass').html('');
                $('#myModal .modal-title').text('Tambah Data');
                $('#myModal').modal('show');
            })
            $('#myModal #myForm').on('submit', function(e) {
                e.preventDefault();
                let id = $('#myForm #id').val();

                let form = new FormData(this);
                $.ajax({
                    url: '{{ route('admin.users.store') }}',
                    type: 'POST',
                    dataType: 'JSON',
                    data: form,
                    processData:false,
                    contentType:false,
                    cache:false,
                    success: function(response) {
                        Swal.fire({
                            position: 'center',
                            icon: 'success',
                            text: response.message,
                            showConfirmButton: true,
                            timer: 1500
                        })
                        otable.ajax.reload();
                        $('#myModal').modal('hide');
                    },
                    error: function(response) {
                        let errors = response.responseJSON?.errors
                        $(form).find('.text-danger.text-small').remove()
                        if (errors) {
                            for (const [key, value] of Object.entries(errors)) {
                                $(`[name='${key}']`).parent().append(
                                    `<sp class="text-danger text-small">${value}</sp>`)
                                $(`[name='${key}']`).addClass('is-invalid')
                            }
                        }
                    }
                })
            })

            $('body').on('click', '.btnEdit', function() {
                let role = $(this).data('role');
                $.ajax({
                    url: '{{ route('admin.roles.get') }}',
                    type: 'POST',
                    dataType: 'JSON',
                    success: function(response) {
                        $('#myModal #role').empty();
                        response.forEach(res => {
                            if(res.name === role)
                            {
                                $('#myModal #role').append(
                                `<option selected class="${res.name}">${res.name}</option>`);
                            }else{
                                $('#myModal #role').append(
                                `<option class="${res.name}">${res.name}</option>`);
                            }
                        });
                    }
                })
                let id = $(this).data('id');
                let name = $(this).data('name');
                let username = $(this).data('username');
                let email = $(this).data('email');

                let status = $(this).data('status');
                $('#myForm #id').val(id);
                $('#myForm #name').val(name);
                $('#myForm #email').val(email);
                $('#myForm #status').val(status);
                $('#myForm #username').val(username);
                $('#myForm .d-pass').html('(Kosongkan jika tidak ingin merubah password.)');
                $('#myModal .modal-title').text('Edit Data');
                $('#myModal').modal('show');
            })
            $('body').on('click', '.btnDelete', function(e) {
                e.preventDefault();
                let id = $(this).data('id');
                let name = $(this).data('name');
                Swal.fire({
                    title: 'Apakah Yakin?',
                    text: `${name} akan dihapus dan tidak bisa dikembalikan!`,
                    icon: 'warning',
                    showCancelButton: true,
                    confirmButtonColor: '#3085d6',
                    cancelButtonColor: '#d33',
                    confirmButtonText: 'Yakin'
                }).then((result) => {
                    if (result.isConfirmed) {
                        $.ajax({
                            url: '{{ url('admin/users/') }}' + '/' + id,
                            type: 'DELETE',
                            dataType: 'JSON',
                            success: function(response) {
                                Swal.fire({
                                    position: 'center',
                                    icon: 'success',
                                    text: response.message,
                                    showConfirmButton: true,
                                    timer: 1500
                                })
                                otable.ajax.reload();
                                $('#myModal').modal('hide');

                            },
                            error: function(response) {
                                let errors = [];
                                $.each(response.responseJSON.errors, function(key,
                                    value) {
                                    errors += `${value}<br>`;
                                });
                                Swal.fire({
                                    position: 'center',
                                    icon: 'error',
                                    html: errors,
                                    showConfirmButton: true,
                                    timer: 1500
                                })
                            }
                        })
                    }
                })
            })

            $('body').on('change','.btnStatus', function(){
                let id = $(this).data('id');
                let status = $(this).val();
                $.ajax({
                    url: '{{ route('admin.users.change-status') }}',
                    type: 'POST',
                    dataType: 'JSON',
                    data: {
                        id:id,
                        status:status
                    },
                    success: function(response) {
                        Swal.fire({
                            position: 'center',
                            icon: 'success',
                            text: response.message,
                            showConfirmButton: true,
                            timer: 1500
                        })
                        otable.ajax.reload();
                        $('#myModal').modal('hide');
                    },
                    error: function(response) {
                        let errors = [];
                        $.each(response.responseJSON.errors, function(key, value) {
                            errors += `${value}<br>`;
                        });
                        Swal.fire({
                            position: 'center',
                            icon: 'error',
                            html: errors,
                            showConfirmButton: true,
                            timer: 1500
                        })
                    }
                })
            })

            $('#myForm #role').select2({
                    theme: 'bootstrap4'
                });

            $('#myModal').on('hidden.bs.modal', function() {
                let form = $('#myModal #myForm');
                $(form).find('.text-danger.text-small').remove();
                $(form).find('.form-control').removeClass('is-invalid');
                form.trigger('reset');
            })
        })
    </script>
